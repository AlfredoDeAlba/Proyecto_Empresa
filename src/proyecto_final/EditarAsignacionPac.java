    public void verificar(String NumEmpleado) throws Excepciones{
        if(NumEmpleado.equals("")){
            throw new Excepciones(1);
        }else if(NumEmpleado.matches("[a-zA-Z]+")){
            throw new Excepciones(2);
        }
    }
